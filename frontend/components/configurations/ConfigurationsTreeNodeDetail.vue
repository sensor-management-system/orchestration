      :device="node.unpack().device"
    >
      <template #mount>
        <base-mount-info
          :mount-action="node.unpack()"
          :editable="editable"
          :deletable="deletable"
          :disabled="node.unpack().device.archived"
          :warning="node.unpack().device.archived ? 'Device is archived. Restore it to edit the mount.' : ''"
          @delete="$emit('delete')"
        />
