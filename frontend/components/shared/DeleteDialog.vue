<template>
  <v-dialog
    v-model="showDialog"
    max-width="360"
    @click:outside="$emit('cancel')"
  >
    <v-card>
      <v-card-title class="headline">
        {{ title }}
      </v-card-title>
      <v-card-text>
        <slot />
      </v-card-text>
      <v-card-actions>
        <v-btn
          text
          :disabled="disabled"
          @click="$emit('cancel')"
        >
          Cancel
        </v-btn>
        <v-spacer />
        <v-btn
          color="error"
          text
          :disabled="disabled"
          @click="$emit('delete')"
        >
          <v-icon
            left
          >
            mdi-delete
          </v-icon>
          Delete
        </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script lang="ts">
import { Component, Prop, Vue } from 'nuxt-property-decorator'

@Component
export default class DeleteDialog extends Vue {
  @Prop({
    required: true,
    type: Boolean
  })
  readonly value!: boolean

  @Prop({
    default: '',
    required: false,
    type: String
  })
  readonly title!: string

  @Prop({
    default: false,
    required: false,
    type: Boolean
  })
  readonly disabled!: boolean

  get showDialog (): boolean {
    return this.value
  }

  set showDialog (value: boolean) {
    this.$emit('input', value)
  }
}
</script>
